export const getChatResponse = (userMessage) => {
  const message = userMessage.toLowerCase();

  // Simple keyword matching for demo purposes
  if (
    message.includes("summarize") ||
    message.includes("summary") ||
    message.includes("key points")
  ) {
    return getRandomResponse("summarize");
  } else if (
    message.includes("findings") ||
    message.includes("main") ||
    message.includes("results")
  ) {
    return getRandomResponse("findings");
  } else if (
    message.includes("chart") ||
    message.includes("graph") ||
    message.includes("visualiz") ||
    message.includes("trends")
  ) {
    return getRandomResponse("chart");
  } else if (
    message.includes("financial") ||
    message.includes("revenue") ||
    message.includes("profit") ||
    message.includes("performance")
  ) {
    return getRandomResponse("financial");
  } else if (
    message.includes("insights") ||
    message.includes("analysis") ||
    message.includes("data")
  ) {
    return getRandomResponse("insights");
  } else {
    return getRandomResponse("default");
  }
};

const getRandomResponse = (category) => {
  const responses = chatResponses[category] || chatResponses.default;
  return responses[Math.floor(Math.random() * responses.length)];
};
